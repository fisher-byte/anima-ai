/**
 * 检测用户消息的意图分类（六类体系）
 *
 * @param query 用户消息
 * @returns 分类名称（'日常生活' | '日常事务' | '学习成长' | '工作事业' | '情感关系' | '思考世界' | '其他'）
 */
export function detectIntent(query: string): string {
  // 去除空格后匹配，兼容"思 考 离 职"这类带空格输入
  const text = query.toLowerCase().replace(/\s+/g, '')

  let bestName = '其他'
  let bestScore = 0
  for (const cat of INTENT_CATEGORIES) {
    const score = cat.keywords.filter(k => text.includes(k)).length
    if (score > bestScore) {
      bestScore = score
      bestName = cat.name
    }
  }
  return bestName
}

